"""Functions for common math operations."""


def add(num1, num2):
    """Return the sum of num1 and num2."""
    return num1 + num2


def subtract(num1, num2):
    return num1 - num2


def multiply(num1, num2):
    return num1 * num2


def divide(num1, num2):
    return num1 / num2


def square(num1):
    square = num1 ** 2
    return square


