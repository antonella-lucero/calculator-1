"""Functions for common math operations."""


def add(num1, num2):
    sum = (num1 + num2)
    return sum


def subtract(num1, num2):
    remain = (num1 - num2)
    return remain


def multiply(num1, num2):
    multiplication = (num1 * num2)
    return multiplication


def divide(num1, num2):
    division = (num1 / num2)
    return division


def square(num1):
    square = num1 ** 2
    return square


def cube(num1):
    cube = num1 ** 3 
    return cube


def power(num1, num2):
    x = pow(num1, num2)
    return x


def mod(num1, num2):
    modulus = num1 % num2
    return modulus
